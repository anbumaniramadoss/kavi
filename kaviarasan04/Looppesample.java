public class Looppesample {
public static void main(String[] args) {
		
		int val=62;
		
		
		
		System.out.println(++val);
		System.out.println(--val);
		System.out.println(++val);
		System.out.println(++val);
		System.out.println(val++);
		
		System.out.println(++val);
		System.out.println(val--);
		System.out.println(val--);
		System.out.println(val++);
		System.out.println(val++);
	}
	}
